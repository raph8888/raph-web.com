<h2 style="text-align: center">Vondelpark</h2>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Vondelpark é o maior parque da cidade e certamente o mais famoso 
parque da Holanda, ele recebe cerca de 10 milhões de visitantes todos os anos. O Vondelpark 
está localizado próximo da praça Leidseplein e dos museus Rijksmuseum, Stedelijk e Van Gogh 
Museum.

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;O Vondelpark é amado por Amsterdammers bem como por turistas, e 
está cheio de pessoas - desfrutando dos raros dias ensolarados, dog-walking, jogging, patinando,
 ouvindo música, vendo as pessoas e descansando na grama. Concertos gratuitos são apresentados no
 teatro ao ar livre e no verão no coreto do parque. Outras atrações são a cúpula de música de
 ferro fundido, o 'Groot Melkhuis" com playground para crianças e o Pavilhão histórico com o
 seu restaurante Vertigo, com um terraço popular no verão.

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;No parque você vai encontrar lindas esculturas: O monumento de bronze do poeta holandês
 Vondel de 3 metros de altura feito por Louis Royer, com o pedestal construido por Pierre Cuypers;
 Um peixe abstrato de concreto de Pablo Picasso; Uma mulher Caribenha de bronze chamada 
'Mama Baranka' do escultor contemporâneo de Amsterdam Nelson Carillho.


