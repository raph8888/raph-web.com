<h2 style="text-align: center">Jordaan Area</h2>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The Jordaan is a district of Amsterdam. The area is bordered by the Lijnbaansgracht canal to the west, the Prinsengracht to the east, the Brouwersgracht to the north and the Leidsegracht to the south. The former canal Rozengracht (now filled in) is the main traffic artery through the neighbourhood.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Originally a working-class neighbourhood, the Jordaan has become one of the most expensive, upscale locations in the Netherlands. It is home to many art galleries, particularly for modern art, and is also dotted with speciality shops and restaurants. Markets are held regularly at Noordermarkt, the Westerstraat (the Lapjesmarkt textile market) and Lindengracht.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rembrandt spent the last years of his life in the Jordaan, on the Rozengracht canal. He was buried in the Westerkerk church, at the corner of Rozengracht and Prinsengracht, just beyond the Jordaan. The Anne Frank House, where Anne Frank went into hiding during World War II, is located on the edge of the Jordaan, on the Prinsengracht canal.</p>

