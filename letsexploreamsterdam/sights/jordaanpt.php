<h2 style="text-align: center">Distrito Jordaan</h2>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;O Jordaan é um lindo distrito de Amsterdam. A área é delimitada pelo canal
 Lijnbaansgracht no oeste, Prinsengracht a leste, Brouwersgracht no norte e Leidsegracht no sul. O antigo
 canal Rozengracht (agora preenchido) é a principal artéria de tráfego através do bairro.

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Originalmente um bairro de classe operária, o Jordaan tornou-se um dos mais
 caros locais de luxo na Holanda. O lar de muitas galerias de arte, em especial de arte moderna,
 também é coberto com lojas especializadas e restaurantes. Mercados são realizados regularmente no 
Noordermarkt, Westerstraat (com o mercado têxtil Lapjesmarkt) e Lindengracht.

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rembrandt passou os últimos anos de sua vida no Jordaan,
 no canal Rozengracht. Ele foi sepultado na igreja Westerkerk, na esquina da Rozengracht e Prinsengracht,
 um pouco além do Jordaan. A Casa de Anne Frank, onde Anne Frank passou a se esconder durante a Segunda Guerra Mundial,
 está localizado à beira do Jordaan, no canal Prinsengracht.

