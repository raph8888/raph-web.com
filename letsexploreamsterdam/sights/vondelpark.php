<h2 style="text-align: center">Vondelpark</h2>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Vondelpark is the largest city park in Amsterdam, and certainly the most famous park in the Netherlands, which welcomes about 10 million visitors every year. The Vondelpark is centrally located south of Leidseplein and near Rijksmuseum, Stedelijk Museum and Van Gogh Museum.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The Vondelpark is loved by Amsterdammers as well as by tourists, and is full of people - enjoying a sunny day, dog-walking, jogging, roller-skating, listening to music, people-watching, or just lazing about in grass. Free concerts are given at the open-air theatre or in the summer at the park's bandstand. Other attractions are the statue of the poet Vondel, the cast iron music dome, the Groot Melkhuis with playground for children, and the historical Pavilion with its restaurant Vertigo, opening in summer a popular terrace.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The imposing 3 meters high bronze monument of the Dutch poet Vondel from 1867 is by Louis Royer, with the pedestal by Pierre Cuypers. An abstract concrete 'Fish' from 1965 is by Pablo Picasso. A bronze representing Caribbean woman called 'Mama Baranka' from 1985 is by Amsterdam contemporary sculptor Nelson Carillho.</p>

