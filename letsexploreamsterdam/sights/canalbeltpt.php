<h2 style="text-align: center">Cintur&atilde;o de Canais</h2>

<p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;O Cintur&atilde;o de Canais - ou Grachtengordel, como os moradores da regi&atilde;o o
 chamam - &eacute; um bairro em Amsterdam que acomoda os principais canais da cidade do s&eacute;culo XVII, localizado
 no centro da cidade, foi adicionado na Lista do Patrim&ocirc;nio Mundial da UNESCO em agosto de 2010. A &aacute;rea
 em torno principais canais da cidade - Singel, Herengracht, Keizersgracht e Prinsensgracht - &eacute; um &iacute;cone 
internacional de planejamento urbano e arquitetura, que ainda est&aacute; intacto depois de quatro s&eacute;culos. </p>

<p> <b> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Singel: </b> O mais antigo dos principais canais e o primeiro que voc&ecirc; vai encontrar
 se come&ccedil;ar o trajeto partindo da Pra&ccedil;a Dam, o canal Singel costumava ser a barreira que forneceu prote&ccedil;&atilde;o 
para Amsterdam. Hoje &eacute; um agitado canal com muitas lojas e caf&eacute;s, onde est&aacute; situado o mundialmente famoso <a href="../sights/flowermarketpt.html">Mercado 
de Flores</a>.</p>

<p> <b> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Herengracht e Keizersgracht: </b>Esses dois canais s&atilde;o os pr&oacute;ximos que voc&ecirc; 
vai encontrar depois do Singel. Eles s&atilde;o os canais que realmente mostram o desejo de Amsterdam de mostrar sua 
import&acirc;ncia e riqueza crescente. Eles s&atilde;o mais largos do que os outros canais e recheados de apartamento luxuosos,
 antigas posses dos novos ricos da classe dos comerciantes da Era de Ouro. Voc&ecirc; vai notar que muitos dos edif&iacute;cios
 aqui parecem pal&aacute;cios. </ P>

<p> <b> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Prinsengracht: </b> O mais distante &agrave; Oeste do Cintur&atilde;o de Canais, Prinsengracht pode significar "canal do pr&iacute;ncipe" em holand&ecirc;s, mas foi o canal onde a vida real acontecia durante a Era de Ouro. Lojas, bares e neg&oacute;cios ferviam no Prinsengracht na &eacute;poca, e ainda o fazem. O Prinsengracht &eacute; onde a casa da Anne Frank e o Westerkerk est&atilde;o localizados. </ P>


