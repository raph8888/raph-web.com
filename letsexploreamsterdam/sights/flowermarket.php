<h2 style="text-align: center">Flower Market</h2>


<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;At the flower market you'll find tulips of every colour - either in bouquets or as bulbs to plant at home. But the market has far more than just tulips. 
No matter what your favourite flower is, you're bound to find it there. Besides cut flowers, 
there are plenty houseplants, seeds and gardening essentials. If you plan to buy bulbs to 
take out of the country, be sure they have a customs cleared stamp so you won't have trouble at the border.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Besides flowers, a number of the shops also sell fun, affordable souvenirs. 
In winter, you'll also find a large variety of Christmas trees and holiday decorations.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Openned Mondays through Saturdays from 9.00h to 17:30h, and on Sundays from 11:00h to 17:30h, 
the market is located On the Singel canal between the Koningsplein and the Muntplein.</p>

