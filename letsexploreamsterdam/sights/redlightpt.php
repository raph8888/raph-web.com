<h2 style="text-align: center">Distrito Vermelho</h2>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;É muito provável que você tenha ouvido algumas histórias únicas sobre
 este bairro e honestamente, provavelmente tudo é verdade. O Distrito Vermelho supera todas as 
expectativas, mas para colocar rumores de lado você tem que verificar por si mesmo, garanto que vai se surpreender.</p>


<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;O Distrito Vermelho ou <i>Rossebuurt</i>, como os moradores daqui o chamam, é diferente de qualquer outro lugar, sem dúvida. O Red Light District que todo mundo conhece é aquele em que mulheres, de todas as nacionalidades, se exibem em vitrines com bordas iluminadas, prontas para colocar em prática a profissão mais antiga do mundo.</p>


<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Outra imagem familiar de Amsterdam no Red Light District é a de vários homens, 
jovens e velhos, casais de mãos dadas apontando em estado de choque, grupos de mulheres rindo, todos comemorando 
uma noite divertida e sem pudor, inúmeros turistas japoneses carregando suas câmeras (exceto na direção das artistas do sexo. Estritamente proibido). Isto é prova suficiente de que o Red Light District merece uma visita. </p>
