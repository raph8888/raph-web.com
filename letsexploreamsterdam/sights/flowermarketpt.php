<h2 style="text-align: center">Flower Market</h2>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;No mercado de flores você encontrará tulipas de todas as cores - 
em ramos ou prontas para plantar em casa. Contudo o mercado tem muito mais do que apenas 
tulipas. Não importa qual é a sua flor favorita você com certeza irá encontrá-la. Além de 
flores de corte, há também plantas de casa, sementes e equipamentos de jardinagem.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Um número de lojas também vendem lembranças
 da cidade e acessórios de jardim. No inverno você também encontra uma grande 
variedade de árvores de Natal e decorações do feriado.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Aberto de segunda-feira à sábados, das 9:00h às 17:30h, 
e aos domingos, das 11:00h às 17:30h, o mercado está localizado no Canal Singel entre 
a Koningsplein o Muntplein.</p>

