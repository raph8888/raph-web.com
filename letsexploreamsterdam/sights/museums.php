<h2 style="text-align: center">Museums</h2>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Amsterdam has a wide variety of fascinating museums catering to all sorts of interests.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Relive the Golden Age by visiting Rembrandt's House or the newly renovated Rijksmuseum; see modern art at Stedelijk, CoBrA or the iconic Van Gogh Museum; experience wartime history at the Anne Frank House or Dutch Resistance Museum. Kids will love the Maritime Museum, NEMO Science Center and Royal Artis Zoo.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;You should seriously consider getting the annual Museumkaart pass if you plan to visit a number of museums and want to cut costs significantly. Alternatively, the Iamsterdam City Card is a scheme run by the tourist board which includes museum admissions and transport for 1-3 days.</p>

