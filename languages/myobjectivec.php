<h3>Objectve C</h3>	<br>
<h4>Definition</h4>	
<p>Objective-C is one of the older languages on the list. 
	Invented in the early 80s as an object oriented version of C, it really is the 
	C language with some extra stuff added to it. That extra stuff makes it object-oriented. 
	The way it's used these days one of the benefits of using Objective-C is it comes 
	with a large library referred to as Cocoa which we will talk about in a moment. It is a 
	compiled language. Not interpreted and not one that uses intermediate bytecode.</p>
	
	<p>Objective-C is the main programming language used by Apple for the OS X and 
	iOS operating systems. A general-purpose, object-oriented programming language 
	that adds Smalltalk-style messaging to the C programming language.</p>

<p>The programming language Objective-C was originally developed in the early 1980s. 
It was selected as the main language used by NeXT for its NeXTSTEP operating system, 
from which OS X and iOS are derived. Generic Objective-C programs that do not use the 
Cocoa or Cocoa Touch libraries, or those using parts that may be ported or reimplemented 
for other systems, can also be compiled for any system supported by GCC or Clang.</p>

	
	<br><br>
	
<h4>Usage</h4>

<p>Objective-C source code program files usually have .m filename extensions, while Objective-C 
header files have .h extensions, the same as for C header files. Objective-C++ 
files are denoted with a .mm file extension.</p>

 <p>Although it's a high-level language, it is one where you need to be a bit more aware 
 	about memory management than things like Java or the .NET languages. It uses something called 
 	reference counting, where we keep track of the objects that we create and whether we're done 
 	with them or not. Although advances in the language means that this is becoming less and less 
 	of an issue, but because of that I wouldn't say it's a particularly friendly language for beginners. 
 	You really need kind of an intermediate level of skill to be messing with Objective-C.</p>

<p>It's one of the stranger looking languages, because there are a quite a few things 
	that aren't shared across other languages. We still have the main section which is denoting 
	where our program begins. But while you'll see are a lot of words that begin with NS everywhere 
	and this is really an old history lesson. Objective-C was used by the company NeXTstep to build 
	their operating system and when that was bought by Apple, it brought a lot of that with it. 
	So when you see example programs written in Objective-C, you will often see this NS letter all 
	over the place.</p>