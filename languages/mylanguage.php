<h3>Web Languages</h3>
		
		<p>Considering that each programming language has been created for a particular 
			task, choosing the right one to work with depends on what you plan to achieve.
			Nevertheless there are tools which without is impossible to build 
			a website, that's the case of HTML and CSS, in one way or another every website 
			that exists uses those technologies, the most popular and functional websites 
			incorporate other technologies with different functionalities.<p>
			
		<p>In software engineering, the terms "front-end" and "back-end" are used 
			to distinct the presentation layer from the data access layer of a website.
			Both of them are achievable by one or more systems, by one or more languages.</p>
			
		<p>The front-end is the interface between the user and the back-end, 
			the technologies used in front-end projects focus on the page design,
			the making of divisions and tables that create the User Interface (UI). 
			The back-ground color, the menu, header, footer, text, images and links, 
			everything that integrates the UI including the content of the website 
			is built by front-end technologies.</p>
				 
		<p>The back-end on the other hand has to do with data processing. One good 
			 way to explain the difference between front and back-end is to think of 
			 how the internet used to work during the 90's for example:</p>
			 
		<p>Back then all the websites we had access to were static, the idea of surfing 
			online was very simple... Reading texts and clicking on links that would take 
			us to another page with other texts and other links, and that was it. 
			The Web was taking its first steps and web developers were basically using plain 
			front-end technology. Nowadays the idea of surfing online is completly different. 
			We have already gotten used to web apps that allow us to store e-mails, share pictures, 
			chat with friends, get directions, and so on. These features require complex coding 
			for data processing that can only be carried out by back-end technologies.</p>
			
