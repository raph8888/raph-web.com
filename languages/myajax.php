<h3>AJAX</h3>
<br>
<h4>Definition</h4>
		
<p>Ajax is a group of interrelated Web development 
	techniques used on the client-side to create asynchronous 
	Web applications. With Ajax, web applications can send data 
	to and retrieve from a server asynchronously (in the background) 
	without interfering with the display and behavior of the existing 
	page. Data can be retrieved using the XMLHttpRequest object. Despite 
	the name, the use of XML is not required (JSON is often used in the 
	AJAJ variant), and the requests do not need to be asynchronous.</p>
		