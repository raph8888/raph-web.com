<h3>HTML<br>HyperText Markup Language - Links and Tags </h3> <br>

<h4 align="middle">The HyperText (Links)</h4>

	<p>The term Hypertext was originated in 1965 by the american pioneer 
		of information technology Ted Nelson. It is used to designate an electronic 
		document text that is connected (hyperlinked) 
		to the other texts (or graphical elements) to which 
		the reader or user is directed. To put this in an easier way to understand, 
		hypertext is the text which contains <a>links</a> to other texts.</p>
		
		<p>HyperMedia in the other hand is a term used for a hypertext element which is not 
			necessarily a text: it can include graphics, 
			video and sound for example. In HTML the links 
			or HyperText elements are the ones surrounded by 
			the tags <b>< a > < /a ></b> and <b>< link > < /link ></b>.</p>

	<h4 align="middle">The MarkUp (Tags)</h4>
	
	<p>The first publicly available description of HTML was a document called "HTML Tags", 
		first mentioned on the Internet by Tim Berners-Lee in late 1991. So the best way to 
		understand HTML is by checking the tags of the websites you visit. 
		Right-click the page's title, image or text and select 'Inspect Element'.</p><br>
		
		<img src="../images/inspect3.png" style="display:block; margin:auto; padding: 5px 5px 5px 5px; border: 2px solid #e8e8e8" /> <br>
		
		<p>Something pops up on the bottom of your page, that's the information about this
			particular page HTML file, you're 'behind the scenes' of a website. Note the presence 
			of the opening and closing tags such as < div > < /div > , < p > < /p > and < img >. 
			The tags are what make the HTML file readable on computers using a web browser. 
			The text in this HTML file (file.html) is marked with these tags so the browser knows 
			what to display.</p>
			
			<p>For instance, the opening tag <b>< h3 ></b> and the closing tag <b>< /h3 ></b> surrounding the 
				title let the browser know the content should be read and displayed as a 'heading'.</p>
				<br>
				
		<img src="../images/htag.jpg" / style="display:block; margin:auto; padding: 5px 5px 5px 5px; border: 2px solid #e8e8e8" /> <br> 
				
				<p>The Inspect Element trick can be done with any element of the page. If you do it with the paragraph 
					you are reading, you'll see the paragrah tags <b>< p ></b> and <b>< /p ></b> surrounding the text. If you do it 
					with the images, you'll get the <b>< img ></b> tag.</p>
					
					<p>The tags let the browser know what's a paragraph, a table, an image. Everything in the HTML 
						file is determined by them, including the links.</p><br>
						
		<img src="../images/tags1.jpg" / style="display:block; margin:auto; padding: 5px 5px 5px 5px; border: 2px solid #e8e8e8" /><br>
		
		<p>HTML is a markup language that web browsers use to interpret and compose text, 
			images and other material into visual or audible web pages. 
			Default characteristics for every item of HTML markup are defined in the browser, 
			and these characteristics can be altered or enhanced by the web page
			designer's additional use of CSS.</p>
