<h3>PHP</h3>
<br>
<h4>Definition</h4>
		
<p>PHP is a server-side scripting language designed 
	for web development but also used as a general-purpose 
	programming language. As of January 2013, PHP was 
	installed on more than 240 million websites (39% of those sampled) 
	and 2.1 million web servers.</p>
		
	